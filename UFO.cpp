int main() {
    bool again = true;
    std::string choice; 
    int w = 0;
    while (again == true) {
        greet();
        srand(time(0));
        w=rand() % (5 - 1 + 1) + 1;
        std::vector<std::string> codeword = {"codecademy","strawberry","friendship","everything","appreciate"};
        std::string answer = "__________";
        int misses = 0;
        std::vector<char> incorrect;
        bool guess = false;
        char letter;
        while (answer != codeword[w] && misses < 7) {
            display_misses(misses);
            display_status(incorrect,answer);
            std::cout << "Please enter your guess: ";
            std::cin >> letter;
            letter = std::tolower(letter, std::locale());
            for (int i = 0; i < codeword[w].length(); i++) {
            if (letter == codeword[w][i]) {
                answer[i] = letter;
                guess = true;
            }
            }
            if (guess) {
            std::cout << "\nCorrect! You're closer to cracking the codeword.\n";
            } else {
            std::cout << "\nIncorrect! The tractor beam pulls the person in further.\n";
            incorrect.push_back(letter);
            misses++;
            }
            guess = false;
        }
        end_game(answer, codeword[w]);
        std::cout << "Would like to play the game again. Y/N\n";
        std::cin >> choice;
        if (choice == "N" || "n") {
            again = false;
        }
        
    }
}
